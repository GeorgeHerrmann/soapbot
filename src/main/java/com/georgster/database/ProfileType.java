package com.georgster.database;

/**
 * An enum that represents the different types of server profiles that can be created in the database.
 */
public enum ProfileType {
    /**
     * Represents a server's user profiles profile.
     */
    PROFILES,
    /**
     * Represents a server's events profile.
     */
    EVENTS,
    /**
     * Represents a server's permissions profile.
     */
    PERMISSIONS
}
